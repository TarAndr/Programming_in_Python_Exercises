# Variables in Python

#boolean
test = 'True'
print(type(test))
print(test)

#int
test = 5
print(type(test))
print(test)

#float
test = 3.14159265
print(type(test))
print(test)

#str
test = "Andrey"
print(type(test))
print(test)

# Operations in Python

print("+ ", 5 + 7)

print("- ", 16 - 9)

print("* ", 3 * 4)

print("/ ", 7 / 3)

print("// ", 7 // 3)

print("** ", 2 ** 8)

print("** ", 9 ** 0.5)

print("% ", 7 % 3)

# Operations priorities

print("Operations priorities: ** | *, /, //, % | +, -")

